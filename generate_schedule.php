                        // Divide the remaining courses into tables by semester
                        $semesterTables = [];
                        foreach ($remainingCourses as $course) {
                            $isCourseCompleted = in_array($course->id, $completedCourses);
                            foreach ($course->semesterAvailability as $semester => $available) {
                                if ($available && !$isCourseCompleted) {
                                    $academicYear = floor(($semester - 1) / 2) + 2023;
                                    $semesterNo = $semester % 2 == 0 ? 2 : 1;
