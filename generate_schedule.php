                                        $semesterTables[$academicYear] = [];
                                    }

                                    if (!isset($semesterTables[$academicYear][$semesterNo])) {
                                        $semesterTables[$academicYear][$semesterNo] = [];
                                    }

                                    $totalHours = array_sum(array_column($semesterTables[$academicYear][$semesterNo], 'hours'));
                                    $allowedHours = 12 - $totalHours;
